boolean isTimeOut(unsigned long& lastTimestamp, unsigned long duration) 
{
  unsigned long currentTimestamp = millis();
  
  if((currentTimestamp - lastTimestamp) < duration) 
  {
    return false;
  }
  
  lastTimestamp = currentTimestamp;

  return true;
}

